package ru.surfstudio.android.mvp.widget.state

/**
 * Состояние восстановления виджета после смерти родителя.
 *
 * Используется для того, чтобы делегаты не получали сообщения ЖЦ родителя сразу же при пересоздании экрана.
 * Создание (onCreate) виджета происходит несколько позже создания родителя (уже после onResume),
 * а поэтому перед рассылкой родительских событий следует дождаться создания виджета.
 *
 * @property NONE обычное состояние, когда виджет показывается на экране и принимает эвенты ЖЦ родителя
 * @property WIDGET_DESTROYED виджет уничтожен и не принимает состояния ЖЦ родителя
 * @property WIDGET_RECOVERING виджет готов к восстановлению и переходу в состояние [NONE]
 */
enum class WidgetRecoveryState {
    NONE,
    WIDGET_DESTROYED,
    WIDGET_RECOVERING
}